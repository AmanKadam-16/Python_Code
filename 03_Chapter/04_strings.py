 # Escape Characters in Strings
 # Escape characters are the character(s) that comes after "\" backslash 
 # Their examples are \n for new line , \t for tab , \' for single quote, \\ for backslash
print("Hello, World \nMy name is Aman")
print("Hello, World.\tMy name is Aman")
print("This is \"India\".")
